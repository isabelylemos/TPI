    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        pref.setCodigo(Integer.parseInt(txtCodigo.getText()));
        pref.setNomePrefeitura(txtPrefeitura.getText());
        pref.setCidade(txtCidade.getText());
        pref.gravar();
        
        ResultSet tabela;
    tabela = null;
    
    tabela = pref.listarPrefeituras();
    DefaultTableModel modelo = (DefaultTableModel) jtblPrefeitura.getModel();
    modelo.setNumRows(0);
    try
    {
        do{
            modelo.addRow(new String[]{tabela.getString(1), tabela.getString(2), tabela.getString(3)});
        }
     while(tabela.next());
    }catch(SQLException erro)
            {
            JOptionPane.showMessageDialog(null, "Erro ao preencher tabela"+ erro) ;    
             }
    }//GEN-LAST:event_btnGravarActionPerformed

